#include "libft.h"

int		ft_atoi(const char *str)
{
	int				i;
	long long int	nb;
	int				n;

	i = 0;
	n = 1;
	nb = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
		n = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		if (nb < 0 && n == -1 && nb != -2147483648)
			return (0);
		if (nb < 0 && n == 1)
			return (-1);
		i++;
	}
	return (nb * n);
}
